package GobalAutomation;

public class StringToLong {

	public static void main(String[] args) {

		// custom string
		String s="99999";

		// printing
		System.out.println(s);

		// converting the string into long by using parseLong
		long l=Long.parseLong(s);

		// printing
		System.out.println(l);
	}
}
